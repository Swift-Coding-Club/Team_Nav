import SwiftUI

extension UIColor {
    class var primary: UIColor? { return UIColor(named: "PrimaryRed") }
    class var secondary: UIColor? { return UIColor(named: "SecondaryOrange") }
    class var redBean: UIColor? { return UIColor(named: "RedBean") }
    class var navBlack: UIColor? { return UIColor(named: "NavBlack") }
    class var navGray: UIColor? { return UIColor(named: "NavGray") }
    class var navWhite: UIColor? { return UIColor(named: "NavWhite") }
}
